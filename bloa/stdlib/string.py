def upper(s: str) -> str: return s.upper()
def lower(s: str) -> str: return s.lower()
def length(s: str) -> int: return len(s)
def reverse(s: str) -> str: return s[::-1]
def concat(a: str, b: str) -> str: return a + b
def split(s: str, sep=" "): return s.split(sep)
def join(lst, sep=" "): return sep.join(lst)
